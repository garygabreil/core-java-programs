/*
 * Variables
 * 		Object have unique set of value ( Theory)
 * 		
 * 		Data-container 
 * 			Data type (Primitive, Non-Primitive type)
 * 				Primitive (system defined)
 * 					int 0,1               - 32 bit   default = 0
 * 					long 012312312321     - 64 bit   default = 0
 * 					float 0.1, 0.2        - 32 bit   default = 0
 * 					double 0.222,0.2231   - 64 bit   default = 0
 * 					boolean false, true   - 1 bit    default = false;
 * 					byte    e1            - 8 bit    default = 0
 * 					char     a, b,        - 16 bit   default = null;
 * 					short    0,2,13       
 * 				Non-Primitive (user-defined)
 * 					String                   		default = null
 * 					Classes								
 * 					Objects
 * 					Interfaces - OOps, Multi-Threading
 * 					Arrays - Collections, 
 * 
 * 
 * 		Syntax:
 * 
 * 		<Data type> <name> = <value>
 * 		eg: int a = 0;
 * 			String str = "Tuesday"
 * 
 * 
 * 		Go Practise
 * 			Should be lowercase, can use underscore, $
 * 
 * */



